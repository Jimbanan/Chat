package Color;

/**
 * Данный класс хранит объекты типа String, хранящие ANSI код для изменения цвета вывода в консоли
 * @author Koziakov Nikolay
 */
public class Color {

    /**  Поле для сброса цвета текста в консоли   */
    public static final String ANSI_RESET = "\u001B[0m";
    /**  Поле для черного цвета текста в консоли   */
    public static final String ANSI_BLACK = "\u001B[30m";
    /**  Поле для красного цвета текста в консоли   */
    public static final String ANSI_RED = "\u001B[31m";
    /**  Поле для зеленого цвета текста в консоли   */
    public static final String ANSI_GREEN = "\u001B[32m";
    /**  Поле для желтого цвета текста в консоли   */
    public static final String ANSI_YELLOW = "\u001B[33m";
    /**  Поле для синего цвета текста в консоли   */
    public static final String ANSI_BLUE = "\u001B[34m";
    /**  Поле для розового цвета текста в консоли   */
    public static final String ANSI_PURPLE = "\u001B[35m";
    /**  Поле для голубого цвета текста в консоли   */
    public static final String ANSI_CYAN = "\u001B[36m";
    /**  Поле для белого цвета текста в консоли   */
    public static final String ANSI_WHITE = "\u001B[37m";

}
